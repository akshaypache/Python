# rules to define identifier
# 1: we can't start identifier name with number
# ex : 2brothers, 7cities, 5boys, 2girls these are not allowed
# 2: use can use underscore(_) in identifier's name
# ex: _start, end_ , middle_name, full_name
# 3: we can use letters numbers and underscore only 
# ex: names2, rollnumber_21,nagpur_city23
# 4 : identifier names are case sensative
# ex : a = 10, A = 20 

