# slicing
str = "Hamare yaha aisa hi hota hai"

print(str)                                # print string
print(str[3])                       # 3rd index of str
print(str[-5])                      # print -5 index of str
print(str[1:4])
print(str[:4])
print(str[4:])
print(str[:])
print(str[-8:-4])
print(str[-4:-8:-1])
print(str[::-1])
print(str[6:10][::-2])
print(str[-8:-2][::-1])