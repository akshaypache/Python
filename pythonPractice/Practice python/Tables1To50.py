# 1 to 50 tables 
import prettytable as pt 
import pyautogui as pg
from time import sleep 
sleep(2)
sp = 41
ep = 50
num = []
for i in range(sp,ep+1):
    num.append(i)

table = pt.PrettyTable(num)

for i in range(1,11):
    line = []
    for j in range(sp,ep+1):
        line.append(f"{i*j}")
    # print(line)
    table.add_row(line)

# print(str(table))

pg.typewrite(str(table),0.01)

for i in range(13):
    pg.hotkey("shift","up")

pg.hotkey("ctrl","/")


